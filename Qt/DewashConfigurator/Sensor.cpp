#include "Sensor.h"

//-------------------------------------------------------------------------------------------------
Sensor::Sensor(int ndx, int type, int pulses, bool enable, QObject *parent)
    : QObject(parent)
    , m_index(ndx)
    , m_type(type)
    , m_pulses(pulses)
    , m_enable(enable)
{

}
//-------------------------------------------------------------------------------------------------
void Sensor::setIndex(int ndx)
{
    m_index = ndx;
}
//-------------------------------------------------------------------------------------------------
int Sensor::getIndex() const
{
    return m_index;
}
//-------------------------------------------------------------------------------------------------
void Sensor::setType(int type)
{
    m_type = type;
}
//-------------------------------------------------------------------------------------------------
int Sensor::getType() const
{
    return m_type;
}
//-------------------------------------------------------------------------------------------------
void Sensor::setPulses(int pulses)
{
    m_pulses = pulses;
}
//-------------------------------------------------------------------------------------------------
int Sensor::getPulses() const
{
    return m_pulses;
}
//-------------------------------------------------------------------------------------------------
void Sensor::setEnable(bool enable)
{
    m_enable = enable;
}
//-------------------------------------------------------------------------------------------------
bool Sensor::isEnable() const
{
    return m_enable;
}
