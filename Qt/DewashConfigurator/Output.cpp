#include "Output.h"

//-------------------------------------------------------------------------------------------------
Output::Output(int ndx, int value, QObject *parent)
    : QObject(parent)
    , m_index(ndx)
    , m_value(value)
{

}
//-------------------------------------------------------------------------------------------------
void Output::setIndex(int ndx)
{
    m_index = ndx;
}
//-------------------------------------------------------------------------------------------------
int Output::getIndex() const
{
    return m_index;
}
//-------------------------------------------------------------------------------------------------
void Output::setValue(int value)
{
    m_value = value;
}
//-------------------------------------------------------------------------------------------------
int Output::getValue() const
{
    return m_value;
}
